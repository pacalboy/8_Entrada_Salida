    private void bAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAbrirActionPerformed
        f = new File(tRuta.getText());
        if (f.exists()) {
            bLeer.setEnabled(true);
            bCerrar.setEnabled(true);
            bAbrir.setEnabled(false);
            lRuta.setEnabled(false);
            try {
                br = new BufferedReader(new FileReader(f));
            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "No se ha encontrado el fichero");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Error al encontrar el fichero");
            tRuta.setText("");
        }    
    }//GEN-LAST:event_bAbrirActionPerformed

    private void bCerrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCerrarActionPerformed
        try {
            br.close();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Error al cerrar el fichero");
        }
        bLeer.setEnabled(false);
        bCerrar.setEnabled(false);
        bAbrir.setEnabled(true);
        lRuta.setEnabled(true);
        tRuta.setText("");
    }//GEN-LAST:event_bCerrarActionPerformed

    private void bLeerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bLeerActionPerformed
        String text;
        try {
            text = br.readLine();
            if (text == null) {
                JOptionPane.showMessageDialog(this, "Fin del fichero");
                bLeer.setEnabled(false);
            }
            lSalida.setText(text);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Error al leer el fichero");
        }
    }//GEN-LAST:event_bLeerActionPerformed

